#include "ft_printf.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t k;

	k = 0;
	i = ft_strlen(s1);
	while (s2[k] != '\0' && k < n)
	{
		s1[i] = s2[k];
		k++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
